package comJdbcTemplatePractice.PatientBean;

public class TemplateLoadReq {
	
	
	private Long templateId;
	private String templateNm;
	private String templateCode;
	private String templateResult;
	
	
	
	public Long getTemplateId() {
		return templateId;
	}
	public void setTemplateId(Long templateId) {
		this.templateId = templateId;
	}
	public String getTemplateNm() {
		return templateNm;
	}
	public void setTemplateNm(String templateNm) {
		this.templateNm = templateNm;
	}
	public String getTemplateCode() {
		return templateCode;
	}
	public void setTemplateCode(String templateCode) {
		this.templateCode = templateCode;
	}
	public String getTemplateResult() {
		return templateResult;
	}
	public void setTemplateResult(String templateResult) {
		this.templateResult = templateResult;
	}
	
	

}
